# slicing 
b="kevin,jose"
print(b[2:5])

# slice from the start
b="kevin,jose"
print(b[:5])

# slice to the end
b="kevin,jose"
print(b[2:])

# negative indexing
b="kevin,jose"
print(b[-5:-2])

# upper case
b="kevin,jose"
print(b.upper())

# lower case
b="kevin,jose"
print(b.lower())

#remove whitespace
b="kevin,jose"
print(b.strip())

# replace string
b="Kevin,Jose"
print(b.replace("K", "J"))

# split string
b="kevin,jose"
print(b.split('j'))

